#include "libft.h"

int	ft_atoi(const char *s)
{
	int		neg;
	long	num;

	num = 0;
	neg = 1;
	while (*s == '\n' || *s == 32 || *s == 13
		|| *s == 9 || *s == 11 || *s == 12)
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			neg = neg * -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		num = (10 * num) + (*s - '0');
		s++;
		if ((neg * num) > 2147483647)
			return (-1);
		else if ((neg * num) < -2147483648)
			return (0);
	}
	return (num * neg);
}
